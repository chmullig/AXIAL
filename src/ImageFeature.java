public class ImageFeature extends Featurable {
    String url;

    public ImageFeature(String s) {
        url = s;
    }

    public void setURL(String s) {
        url = s;
    }

    public String getURL() {
        return url;
    }

    public void draw(PGraphics pg) {
        pg.pushStyle();
        pg.noStroke();
        pg.fill(0, 150, 187, alphaLevel);
        pg.text(url, p.x, p.y);
        pg.popStyle();
    }
}
